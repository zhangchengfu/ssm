package com.laozhang.corejava.day02;

/**
 * 本类用来演示特殊符号
 */
public class CodeDemo {
	public static void main(String[] args) {
		// 隔开一个tab距离
		System.out.println("\t" + "你好!");
		// 换行
		// System.out.println();
		System.out.print("\n");
		System.out.println("你好!");

		// 输出我的nba资料在D盘下的nba文件夹中
		System.out.println("我的NBA资料在D:\\nba文件夹中");
		System.out.println("韩梅梅说:\"李磊你妹啊!\"");
	}
}
